    public String getParentRefrencePath() {
        return parentRefrencePath;
    }

    public void setParentRefrencePath(String parentRefrencePath) {
        this.parentRefrencePath = parentRefrencePath;
    }

    public String getImgType() {
        return imgType;
    }

    public void setImgType(String imgType) {
        this.imgType = imgType;
    }
